use lexer::Lexer;
use std::slice;


struct TestCase {
    data: String,
    expected: Vec<String>,
}

struct Test {
    test_cases: Vec<TestCase>,
}

impl Test {
    fn iter(&self) -> TestIter {
        TestIter {
            inner: self.test_cases.iter(),
        }
    }
}

struct TestIter<'a> {
    inner: slice::Iter<'a, TestCase>,
}

impl<'a> Iterator for TestIter<'a> {
    type Item = &'a TestCase;

    fn next(&mut self) -> Option<Self::Item> {
        self.inner.next()
    }
}


